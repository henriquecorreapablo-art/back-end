# 1. Multiplicação vem antes da soma
# Cálculo: 5 + (3 * 2) = 5 + 6 = 11
print(5 + 3 * 2) 

# 2. Potenciação (**) vem antes da multiplicação (*) e da soma (+)
# Cálculo: (3 * 5) + (4 ** 2) = 15 + 16 = 31
print(3 * 5 + 4 ** 2)

# 3. Parênteses têm prioridade máxima, depois a potência
# Cálculo: 3 * (9 ** 2) = 3 * 81 = 243
print(3 * (5 + 4) ** 2)

